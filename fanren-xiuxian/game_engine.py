def get_available_choices_for_scene(scene_id: str, game_state: GameState = None) -> List[dict]:
    """获取某场景的可用选项（可选按游戏状态过滤条件）"""
    scene = SCENES.get(scene_id)
    if not scene:
        return []
    choices = scene['choices']
    if game_state is None:
        return list(choices)
    # 过滤条件
    available = []
    for ch in choices:
        if ch.get('cond'):
            cond = ch['cond']
            if 'aff' in cond:
                skip = False
                for npc_id, min_val in cond['aff'].items():
                    if game_state.affection.get(npc_id, 50) < min_val:
                        skip = True
                        break
                if skip:
                    continue
            if 'flag' in cond:
                if not game_state.knowledge.get(cond['flag']):
                    continue
            if 'notFlag' in cond:
                if game_state.knowledge.get(cond['notFlag']):
                    continue
        available.append(ch)
    return available


