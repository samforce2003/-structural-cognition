# ==================== GameState 类 ====================

class GameState:
    """
    管理《凡人修仙传 · 药引》的游戏状态。
    """

    def __init__(self):
        # 基础状态
        self.day: int = 1
        self.period: int = 0  # 0=卯时·晨, 1=午时·日, 2=酉时·暮, 3=子时·夜
        self.cd: int = 7
        self.loc: str = ''  # yaolu / wuchang / houshan / xiangfang
        self.weather: str = ''
        self.scene: str = ''  # 当前场景ID

        # NPC相关
        self.daily_npc: Dict[str, int] = {}  # 当天与NPC交互次数
        self.npcs: Dict[str, dict] = {}  # NPC状态
        self.affection: Dict[str, int] = {}  # 好感度
        self.npc_rel: Dict[str, int] = {
            'modaifu_lifeiyu': 35,
            'modaifu_zhangxiu': 70,
            'lifeiyu_zhangxiu': 45,
        }

        # 剧情标记
        self.knowledge: Dict[str, bool] = {}
        self.flags: Dict[str, bool] = {}
        self.history: List[str] = []  # 场景历史

        # 初始化
        self._init_npcs()
        if not self.weather:
            self._new_weather()

    def _init_npcs(self):
        """初始化NPC状态"""
        default_aff = {
            'zhangxiu': 60,
            'lifeiyu': 40,
            'modaifu': 50,
        }
        self.npcs = {}
        for npc_id, npc_data in NPCS.items():
            if npc_id not in self.affection:
                self.affection[npc_id] = default_aff.get(npc_id, 50)
            alv = aff_lv(self.affection[npc_id])
            self.npcs[npc_id] = {
                'mood': 'neutral',
                'trust': self.affection[npc_id],
                'known': False,
                'aff_lv': alv['n'],
            }

    def _new_weather(self):
        """随机生成天气"""
        self.weather = random.choice(WEATHERS)

    def _reset_daily(self):
        """重置每日NPC交互计数"""
        self.daily_npc = {}

    # ==================== NPC好感度系统 ====================

    def aff_delta(self, npc_id: str, delta: int) -> dict:
        """
        更改NPC好感度，并触发冲突矩阵的涟漪效应。
        返回变化结果：{new_score, level, ripple: [受影响的NPC名称]}
        """
        if not self.affection:
            self.affection = {}
        default_vals = {'zhangxiu': 60, 'lifeiyu': 40, 'modaifu': 50}
        if npc_id not in self.affection:
            self.affection[npc_id] = default_vals.get(npc_id, 50)

        old = self.affection[npc_id]
        self.affection[npc_id] = max(0, min(100, old + delta))

        # 冲突涟漪
        ripple = []
        conf = NPC_CONFLICTS.get(npc_id, {})
        for oid, cd in conf.items():
            if oid == npc_id or cd >= 0:
                continue
            oo = self.affection.get(oid, default_vals.get(oid, 50))
            change = cd * (1 if delta > 0 else (-1 if delta < 0 else 0))
            self.affection[oid] = max(0, min(100, oo + change))
            if abs(cd) >= 2 and abs(change) >= 2:
                ripple.append(NPCS[oid]['name'])

        # 更新NPC状态
        if npc_id in self.npcs:
            self.npcs[npc_id]['trust'] = self.affection[npc_id]
            alv = aff_lv(self.affection[npc_id])
            self.npcs[npc_id]['aff_lv'] = alv['n']

        return {
            'new_score': self.affection[npc_id],
            'level': aff_lv(self.affection[npc_id])['n'],
            'ripple': ripple,
        }

    def npc_rel_get(self, a: str, b: str) -> int:
        """获取两个NPC之间的关系值"""
        k = '_'.join(sorted([a, b]))
        return self.npc_rel.get(k, 50)

    def npc_rel_delta(self, a: str, b: str, d: int):
        """更改两个NPC之间的关系值"""
        k = '_'.join(sorted([a, b]))
        old = self.npc_rel.get(k, 50)
        self.npc_rel[k] = max(0, min(100, old + d))

    def npc_rel_tracking(self):
        """跟踪当天互动的NPC，适当增加关系"""
        npc_ids = list(self.daily_npc.keys())
        if len(npc_ids) >= 2:
            for i in range(len(npc_ids)):
                for j in range(i + 1, len(npc_ids)):
                    self.npc_rel_delta(npc_ids[i], npc_ids[j], 1)

    # ==================== 时间系统 ====================

    def advance_time(self, periods: int = 1) -> Optional[str]:
        """
        推进时间。返回 'ending_a' 如果倒计时归零，否则返回 None。
        """
        old_day = self.day
        self.period += periods
        while self.period >= 4:
            self.period -= 4
            self.day += 1
            self.cd -= 1
        if self.day > old_day:
            self._reset_daily()
            self._new_weather()
        if self.cd <= 0:
            return 'ending_a'
        return None

    # ==================== 场景系统 ====================

    def get_current_scene(self) -> dict:
        """
        返回当前场景数据：
        {
            'scene_id': str,
            'title': str,
            'loc': str,
            'text': str,  # 完整的场景文本（含天气前缀）
            'weather': str,
            'period': str,
            'day': int,
            'cd': int,
            'choices': [...],  # 可用选项（已过滤条件）
        }
        """
        if not self.scene or self.scene not in SCENES:
            return {
                'scene_id': '',
                'title': '',
                'loc': '',
                'text': '',
                'weather': self.weather,
                'period': PERIODS[self.period] if self.period < len(PERIODS) else '',
                'day': self.day,
                'cd': self.cd,
                'choices': [],
                'error': 'No active scene',
            }

        scene = SCENES[self.scene]
        self.loc = scene['loc']

        # 天气前缀
        weather_prefix = WEATHER_DESC.get(self.weather, '')
        full_text = weather_prefix + '\n\n' + scene['text'] if weather_prefix else scene['text']

        # 过滤可用选项（根据条件）
        available_choices = []
        for ch in scene['choices']:
            if ch.get('cond'):
                cond = ch['cond']
                # 好感度条件
                if 'aff' in cond:
                    skip = False
                    for npc_id, min_val in cond['aff'].items():
                        if self.affection.get(npc_id, 50) < min_val:
                            skip = True
                            break
                    if skip:
                        continue
                # flag 条件
                if 'flag' in cond:
                    if not self.knowledge.get(cond['flag']):
                        continue
                # notFlag 条件
                if 'notFlag' in cond:
                    if self.knowledge.get(cond['notFlag']):
                        continue
            available_choices.append(ch)

        return {
            'scene_id': self.scene,
            'title': scene['title'],
            'loc': scene['loc'],
            'text': full_text,
            'weather': self.weather,
            'period': PERIODS[self.period] if self.period < len(PERIODS) else '',
            'day': self.day,
            'cd': self.cd,
            'choices': available_choices,
        }

    def choose_option(self, choice_index: int) -> dict:
        """
        执行选择：
        1. 更新好感度
        2. 更新NPC关系
        3. 更新knowledge
        4. 推进时间（如果有disp）
        5. 跳转到下一场景

        返回：
        {
            'success': bool,
            'scene': dict,  # 下一场景数据
            'effects': {    # 效果汇总
                'aff_changes': [...],
                'time_advanced': bool,
                'knowledge_gained': [...],
            },
            'error': str or None,
        }
        """
        # 1. 获取当前场景
        if not self.scene or self.scene not in SCENES:
            return {'success': False, 'scene': None, 'effects': {}, 'error': 'No active scene'}

        scene = SCENES[self.scene]

        # 过滤可用选项
        available_choices = []
        for ch in scene['choices']:
            if ch.get('cond'):
                cond = ch['cond']
                if 'aff' in cond:
                    skip = False
                    for npc_id, min_val in cond['aff'].items():
                        if self.affection.get(npc_id, 50) < min_val:
                            skip = True
                            break
                    if skip:
                        continue
                if 'flag' in cond:
                    if not self.knowledge.get(cond['flag']):
                        continue
                if 'notFlag' in cond:
                    if self.knowledge.get(cond['notFlag']):
                        continue
            available_choices.append(ch)

        if choice_index < 0 or choice_index >= len(available_choices):
            return {
                'success': False,
                'scene': None,
                'effects': {},
                'error': f'Invalid choice index {choice_index}. Available: {len(available_choices)}',
            }

        choice = available_choices[choice_index]
        effects = {
            'aff_changes': [],
            'time_advanced': False,
            'knowledge_gained': [],
            'ripple': [],
        }

        # 2. 记录每日NPC交互
        # 根据场景位置推断当前活跃NPC
        loc_npc_map = {'yaolu': 'modaifu', 'wuchang': 'lifeiyu', 'houshan': 'zhangxiu'}
        active_npc = loc_npc_map.get(scene['loc'])
        if active_npc:
            self.daily_npc[active_npc] = self.daily_npc.get(active_npc, 0) + 1

        # 3. 处理好感度变化
        if choice.get('bond'):
            for n, d in choice['bond'].items():
                result = self.aff_delta(n, d)
                effects['aff_changes'].append({
                    'npc': n,
                    'name': NPCS.get(n, {}).get('name', n),
                    'delta': d,
                    'new_score': result['new_score'],
                    'level': result['level'],
                })
                if result['ripple']:
                    effects['ripple'].extend(result['ripple'])
            self.npc_rel_tracking()

        if choice.get('aff'):
            for n, d in choice['aff'].items():
                result = self.aff_delta(n, d)
                effects['aff_changes'].append({
                    'npc': n,
                    'name': NPCS.get(n, {}).get('name', n),
                    'delta': d,
                    'new_score': result['new_score'],
                    'level': result['level'],
                })
                if result['ripple']:
                    effects['ripple'].extend(result['ripple'])
            self.npc_rel_tracking()

        # 4. 修罗场检测
        diff_npc_count = len(self.daily_npc)
        if diff_npc_count >= 3:
            cold = []
            for nid, count in self.daily_npc.items():
                if count <= 1 and self.affection.get(nid, 50) >= 55:
                    self.aff_delta(nid, -2)
                    cold.append(NPCS[nid]['name'])
            if cold:
                effects['aff_changes'].append({
                    'note': f'修罗场：{"、".join(cold)}注意到了你的轻浮',
                })

        # 5. 更新knowledge
        if choice.get('know'):
            for k in choice['know']:
                self.knowledge[k] = True
                effects['knowledge_gained'].append(k)

        # 6. 记录历史
        self.history.append(self.scene)

        # 7. 推进时间
        if choice.get('disp'):
            ending = self.advance_time(1)
            effects['time_advanced'] = True
            if ending:
                self.scene = ending
                scene_data = self.get_current_scene()
                effects['ending_triggered'] = True
                return {
                    'success': True,
                    'scene': scene_data,
                    'effects': effects,
                    'error': None,
                }

        # 8. 跳转场景
        next_scene_id = choice['next']
        self.scene = next_scene_id
        scene_data = self.get_current_scene()

        return {
            'success': True,
            'scene': scene_data,
            'effects': effects,
            'error': None,
        }

    def start_game(self, scene_id: str = 'd1_m_courtyard') -> dict:
        """开始或重新开始游戏"""
        self.day = 1
        self.period = 0
        self.cd = 7
        self.loc = ''
        self.daily_npc = {}
        self.knowledge = {}
        self.flags = {}
        self.history = []
        self.affection = {}
        self.npc_rel = {
            'modaifu_lifeiyu': 35,
            'modaifu_zhangxiu': 70,
            'lifeiyu_zhangxiu': 45,
        }
        self._init_npcs()
        self._new_weather()
        self._reset_daily()
        self.scene = scene_id
        return self.get_current_scene()

    # ==================== NPC状态查询 ====================

    def get_npc_schedule(self, npc_id: str) -> str:
        """获取NPC当前时段在当前位置的活动描述"""
        if npc_id not in SCHEDULES or self.period not in SCHEDULES[npc_id]:
            return '不在附近'
        s = SCHEDULES[npc_id][self.period]
        if self.loc and s.get(self.loc):
            return s[self.loc]
        # 尝试找第一个有活动的loc
        for loc_key, act in s.items():
            if act:
                return f'在{LOC_NAMES.get(loc_key, loc_key)} · {act.split("·")[0]}'
        return '不在附近'

    def get_npc_status(self) -> List[dict]:
        """
        返回所有NPC状态：
        [
            {
                'id': str,
                'name': str,
                'avatar': str,
                'title': str,
                'desc': str,
                'affection': int,
                'aff_level': str,
                'mood': str,
                'trust': int,
                'schedule': str,  # 当前活动
            },
            ...
        ]
        """
        result = []
        for npc_id, npc_data in NPCS.items():
            aff = self.affection.get(npc_id, 50)
            alv = aff_lv(aff)
            schedule = self.get_npc_schedule(npc_id)
            npc_state = self.npcs.get(npc_id, {'mood': 'neutral', 'trust': aff, 'aff_lv': alv['n']})
            result.append({
                'id': npc_id,
                'name': npc_data['name'],
                'avatar': npc_data['avatar'],
                'title': npc_data['title'],
                'desc': npc_data['desc'],
                'affection': aff,
                'aff_level': alv['n'],
                'mood': npc_state.get('mood', 'neutral'),
                'trust': npc_state.get('trust', aff),
                'schedule': schedule,
            })
        return result

    # ==================== 状态摘要 ====================

    def get_state_summary(self) -> str:
        """
        返回游戏状态摘要（给AI看的自然语言摘要）。
        """
        period_name = PERIODS[self.period] if self.period < len(PERIODS) else '未知'
        loc_name = LOC_NAMES.get(self.loc, self.loc or '未知')
        scene_title = SCENES[self.scene]['title'] if self.scene in SCENES else '无'

        summary = f'【游戏状态】\n'
        summary += f'- 第{self.day}天 · {period_name} · {self.weather}\n'
        summary += f'- 位置：{loc_name}（{scene_title}）\n'
        summary += f'- 寒毒倒计时：{self.cd}天\n\n'

        summary += f'【NPC好感度】\n'
        for npc_id, npc_data in NPCS.items():
            aff = self.affection.get(npc_id, 50)
            alv = aff_lv(aff)
            schedule = self.get_npc_schedule(npc_id)
            summary += f'- {npc_data["avatar"]} {npc_data["name"]}：{alv["n"]}({aff}) — {schedule}\n'

        # NPC关系
        summary += f'\n【NPC之间的关系】\n'
        for pair in ['modaifu_lifeiyu', 'modaifu_zhangxiu', 'lifeiyu_zhangxiu']:
            val = self.npc_rel.get(pair, 50)
            a, b = pair.split('_')
            a_name = NPCS.get(a, {}).get('name', a)
            b_name = NPCS.get(b, {}).get('name', b)
            summary += f'- {a_name}↔{b_name}：{val}/100\n'

        # 已知信息
        if self.knowledge:
            summary += f'\n【已知信息】\n'
            for k, v in self.knowledge.items():
                if v:
                    summary += f'- {k}\n'

        # 最近历史
        if self.history:
            recent = self.history[-3:]
            summary += f'\n【最近场景】\n'
            for sid in recent:
                s = SCENES.get(sid, {})
                summary += f'- {s.get("title", sid)}\n'

        return summary

    def to_dict(self) -> dict:
        """导出完整状态为字典（用于存档）"""
        return {
            'day': self.day,
            'period': self.period,
            'cd': self.cd,
            'loc': self.loc,
            'weather': self.weather,
            'scene': self.scene,
            'daily_npc': dict(self.daily_npc),
            'npcs': copy.deepcopy(self.npcs),
            'affection': dict(self.affection),
            'npc_rel': dict(self.npc_rel),
            'knowledge': dict(self.knowledge),
            'flags': dict(self.flags),
            'history': list(self.history),
        }

    @classmethod
    def from_dict(cls, data: dict) -> 'GameState':
        """从字典恢复状态（用于读档）"""
        gs = cls.__new__(cls)
        gs.day = data.get('day', 1)
        gs.period = data.get('period', 0)
        gs.cd = data.get('cd', 7)
        gs.loc = data.get('loc', '')
        gs.weather = data.get('weather', '')
        gs.scene = data.get('scene', '')
        gs.daily_npc = dict(data.get('daily_npc', {}))
        gs.npcs = copy.deepcopy(data.get('npcs', {}))
        gs.affection = dict(data.get('affection', {}))
        gs.npc_rel = dict(data.get('npc_rel', {
            'modaifu_lifeiyu': 35,
            'modaifu_zhangxiu': 70,
            'lifeiyu_zhangxiu': 45,
        }))
        gs.knowledge = dict(data.get('knowledge', {}))
        gs.flags = dict(data.get('flags', {}))
        gs.history = list(data.get('history', []))
        if not gs.weather:
            gs._new_weather()
        return gs

    def save_to_json(self, filepath: str):
        """存档到JSON文件"""
        data = self.to_dict()
        with open(filepath, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)

    @classmethod
    def load_from_json(cls, filepath: str) -> 'GameState':
        """从JSON文件读档"""
        with open(filepath, 'r', encoding='utf-8') as f:
            data = json.load(f)
        return cls.from_dict(data)


